    private void exibirSaudacao(){
        String saudacao;
        int hora = java.time.LocalTime.now().getHour();
        
        if(hora >= 5 && hora < 12){
            saudacao = "Bom dia";
        } else if (hora >= 12 && hora < 18){
            saudacao = "Boa tarde";
        } else {
            saudacao = "Boa noite";
        }
        
        labelApresentarUsername.setText(saudacao + ", " + usuario.getNome());
    }

